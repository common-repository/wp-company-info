<h1>Company Information</h1>
<p>Information pertaining specifically to your business/company such as your logo, address, phone number, email address, and more are stored within Wordpress and can be displayed using <em>shortcodes</em>.</p>


<h2>Branding</h2>
<p>Branding is the logo for your website.  To upload a new logo, go to Settings > <strong>Branding</strong>.  Using the [logo] shortcode will output the logo image.  There is one 'size' parameter available for this shortcode:  [logo size="thumbnail"]`</p>


<h2>Contact Information</h2>
<p>Contact information is things like your address, phone number, and email address.  To edit this information, go to Settings > <strong>Contact Information</strong>.  There are various shortcodes that can be used throughout the website to display these values:</p>

<ul>
	<li><strong>Full Address</strong> &mdash; [address]</li>
	<li><strong>Address (Line 1)</strong> &mdash; [address field="address-1"]</li>
	<li><strong>Address (Line 2)</strong> &mdash; [address field="address-2"]</li>
	<li><strong>City</strong> &mdash; [address field="city"]</li>
	<li><strong>State</strong> &mdash; [address field="state"]</li>
	<li><strong>Zip</strong> &mdash; [address field="zip"]</li>
	<li><strong>Phone Number</strong> &mdash; [phone]</li>
	<li><strong>Fax Number</strong> &mdash; [fax]</li>
	<li><strong>Email</strong> &mdash; [email]</li>
</ul>


<h2>Social Network Links</h2>
<p>The social network links for your website can be added by going to Settings > <strong>Social Network Links</strong>.  To output the links, use the following shortcode:</p>

<p>[social-networks class="class-name-here" icons="true|false" size="lg|2x|3x|4x|5x" square="true|false" styles="true|false"]</p>